class Author {
  int id;
  String firstName;
  String lastName;
  String middleName;
  String dateOfBirth;
  String dateOfDeath;
  String country;

  Author({
    required this.id,
    required this.lastName,
    required this.firstName,
    required this.middleName,
    required this.dateOfBirth,
    required this.dateOfDeath,
    required this.country
  });
}